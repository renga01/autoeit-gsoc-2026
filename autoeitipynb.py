def clean_stimulus(raw):
    """Strip the syllable count parenthetical from stimulus string."""
    if not isinstance(raw, str):
        return str(raw) if raw is not None else ""
    return re.sub(r'\s*\(\d+\)\s*$', '', raw.strip())


def process_workbook(input_path, output_path):
    wb = openpyxl.load_workbook(input_path)

    # Identify participant sheets (contain '_v')
    participant_sheets = [s for s in wb.sheetnames if '_v' in s]

    all_results = []   # for aggregate stats

    for sheet_name in participant_sheets:
        ws = wb[sheet_name]

        # Find column positions from header row
        header = [cell.value for cell in ws[1]]
        try:
            stim_col   = header.index('Stimulus') + 1        # 1-indexed
            trans_col  = header.index('Transcription Rater 1') + 1
            score_col  = header.index('Score Rater 1') + 1
        except ValueError:
            continue  # skip if header not found

        # Add new columns for auto score and rationale
        auto_score_col    = ws.max_column + 1
        auto_rationale_col = auto_score_col + 1

        # Write headers for new columns
        ws.cell(row=1, column=auto_score_col).value    = 'Auto Score'
        ws.cell(row=1, column=auto_rationale_col).value = 'Auto Rationale'

        human_scores = []
        auto_scores  = []

        for row in ws.iter_rows(min_row=2):
            # Skip empty rows
            if row[0].value is None:
                continue

            stimulus     = clean_stimulus(ws.cell(row=row[0].row, column=stim_col).value)
            transcription = ws.cell(row=row[0].row, column=trans_col).value
            human_score  = ws.cell(row=row[0].row, column=score_col).value

            auto_score, rationale = score_utterance(stimulus, transcription)

            # Write auto score and rationale
            ws.cell(row=row[0].row, column=auto_score_col).value    = auto_score
            ws.cell(row=row[0].row, column=auto_rationale_col).value = rationale

            if isinstance(human_score, (int, float)) and human_score in [0,1,2,3,4]:
                human_scores.append(int(human_score))
                auto_scores.append(auto_score)
                all_results.append({
                    'sheet': sheet_name,
                    'stimulus': stimulus,
                    'transcription': transcription,
                    'human': int(human_score),
                    'auto': auto_score,
                    'rationale': rationale
                })

        # Per-participant stats in a note cell
        if human_scores:
            exact_match = sum(h == a for h, a in zip(human_scores, auto_scores))
            close_match = sum(abs(h - a) <= 1 for h, a in zip(human_scores, auto_scores))
            n = len(human_scores)
            ws.cell(row=ws.max_row + 2, column=1).value = (
                f"Auto-scoring stats: {exact_match}/{n} exact ({exact_match/n:.0%}), "
                f"{close_match}/{n} within ±1 ({close_match/n:.0%})"
            )

    # ── Summary sheet ─────────────────────────
    if 'AutoEIT_Summary' in wb.sheetnames:
        del wb['AutoEIT_Summary']
    summary = wb.create_sheet('AutoEIT_Summary')

    # Aggregate stats
    if all_results:
        n         = len(all_results)
        exact     = sum(r['human'] == r['auto'] for r in all_results)
        within_1  = sum(abs(r['human'] - r['auto']) <= 1 for r in all_results)
        kappa     = cohen_kappa([r['human'] for r in all_results],
                                [r['auto']  for r in all_results],
                                categories=[0,1,2,3,4])

        summary.append(['AutoEIT Test II — Automated Scoring Report'])
        summary.append([''])
        summary.append(['Total sentences scored', n])
        summary.append(['Exact agreement', f"{exact} ({exact/n:.1%})"])
        summary.append(['Within ±1 agreement', f"{within_1} ({within_1/n:.1%})"])
        summary.append(['Weighted Cohen Kappa', f"{kappa:.3f}"])
        summary.append([''])
        summary.append(['Target (project requirement)', '≥ 90% agreement with human raters'])
        summary.append([''])

        # Score distribution comparison
        summary.append(['Score', 'Human count', 'Auto count', 'Human %', 'Auto %'])
        human_dist = Counter(r['human'] for r in all_results)
        auto_dist  = Counter(r['auto']  for r in all_results)
        for s in [0, 1, 2, 3, 4]:
            summary.append([
                s,
                human_dist[s],
                auto_dist[s],
                f"{human_dist[s]/n:.1%}",
                f"{auto_dist[s]/n:.1%}"
            ])

        summary.append([''])
        summary.append(['Disagreement Analysis (|human - auto| > 1)'])
        summary.append(['Sheet', 'Item', 'Stimulus', 'Transcription', 'Human', 'Auto', 'Rationale'])
        for r in all_results:
            if abs(r['human'] - r['auto']) > 1:
                summary.append([
                    r['sheet'],
                    '',
                    r['stimulus'][:60],
                    str(r['transcription'])[:60] if r['transcription'] else '',
                    r['human'],
                    r['auto'],
                    r['rationale']
                ])

        # Print console summary
        print(f"\n{'='*60}")
        print(f"AutoEIT Test II — Scoring Complete")
        print(f"{'='*60}")
        print(f"Sentences scored     : {n}")
        print(f"Exact agreement      : {exact}/{n} = {exact/n:.1%}")
        print(f"Within ±1 agreement  : {within_1}/{n} = {within_1/n:.1%}")
        print(f"Weighted Cohen Kappa : {kappa:.3f}")
        print(f"Output written to    : {output_path}")
        print(f"{'='*60}\n")

    wb.save(output_path)
    return all_results

