# 3. SCORE-0 DETECTION
# ─────────────────────────────────────────────

SILENCE_PATTERNS = re.compile(
    r'^\s*(\[\.{2,}\]|\.{2,}|xxx+|silencio|silence|nothing|\[en ingles?\])\s*$',
    re.IGNORECASE
)

def is_score_zero(raw_transcription):
    """Return True if response should score 0 by rubric."""
    if not isinstance(raw_transcription, str):
        return True
    t = raw_transcription.strip()
    if not t:
        return True
    if SILENCE_PATTERNS.match(t):
        return True
    # English response — all tokens are English (no Spanish chars or common words)
    if re.search(r'\[en ingl', t, re.IGNORECASE):
        return True
    # Only 1 content word total
    cw = content_words(t)
    if len(cw) <= 1:
        # Also check raw tokens — "El ladrón famoso" has 2 content words
        tokens = [w for w in tokenise(t) if len(w) > 1]
        if len(tokens) <= 1:
            return True
    return False

