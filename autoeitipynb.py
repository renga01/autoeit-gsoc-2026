# 7. WEIGHTED COHEN'S KAPPA (linear weights)
# ─────────────────────────────────────────────

def cohen_kappa(y1, y2, categories):
    """Compute weighted Cohen's Kappa with linear weights."""
    n = len(y1)
    k = len(categories)
    cat_idx = {c: i for i, c in enumerate(categories)}

    # Observed agreement matrix
    obs = [[0]*k for _ in range(k)]
    for a, b in zip(y1, y2):
        if a in cat_idx and b in cat_idx:
            obs[cat_idx[a]][cat_idx[b]] += 1

    # Marginals
    row_sum = [sum(obs[i]) for i in range(k)]
    col_sum = [sum(obs[i][j] for i in range(k)) for j in range(k)]

    # Weighted observed and expected
    w_obs = 0.0
    w_exp = 0.0
    for i in range(k):
        for j in range(k):
            weight = 1 - abs(i - j) / (k - 1)   # linear weight
            w_obs += weight * obs[i][j]
            w_exp += weight * (row_sum[i] * col_sum[j] / n)

    w_obs /= n
    w_exp /= n

    if 1 - w_exp == 0:
        return 1.0
    return (w_obs - w_exp) / (1 - w_exp)

