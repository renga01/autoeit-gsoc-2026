# 4. MEANING-CHANGE & GRAMMAR DETECTORS
# ─────────────────────────────────────────────

# Grammatical structures whose loss signals score degradation
# (relative clause markers, reflexive-indirect constructions)
GRAMMAR_PATTERNS = [
    (r'\bal que\b', 'relative pronoun "al que" dropped to "que"'),
    (r'\bse le\b',  'indirect reflexive "se le" simplified'),
]

def has_grammar_degradation(prompt_norm, learner_norm):
    """
    Detect loss of key grammatical structures that human raters penalise.
    E.g. 'al que' → 'que' (relative pronoun degradation) caps score at 3.
    """
    for (pattern, _) in GRAMMAR_PATTERNS:
        if re.search(pattern, prompt_norm) and not re.search(pattern, learner_norm):
            return True
    return False

# Word pairs that flip meaning — if learner substitutes these, cap at 2
MEANING_OPPOSITES = [
    ('negro', 'blanco'), ('blanco', 'negro'),
    ('baratos', 'caros'), ('caros', 'baratos'),
    ('dificil', 'facil'), ('facil', 'dificil'),
    ('fuman', 'beben'), ('beben', 'fuman'),
    ('murio', 'nacio'), ('triste', 'feliz'), ('feliz', 'triste'),
    ('bajar', 'subir'), ('subir', 'bajar'),
    ('disminuido', 'incrementado'), ('incrementado', 'disminuido'),
    ('calor', 'frio'), ('frio', 'calor'),
    ('gusta', 'odia'), ('odia', 'gusta'),
    ('bien', 'mal'), ('mal', 'bien'),
]

def has_meaning_flip(prompt_norm, learner_norm):
    """Detect if learner substituted an antonym — meaning changed."""
    for (a, b) in MEANING_OPPOSITES:
        if a in prompt_norm and b in learner_norm and a not in learner_norm:
            return True
    return False

def negation_added_or_removed(prompt_norm, learner_norm):
    """Detect negation change (no added/removed)."""
    prompt_has_no = bool(re.search(r'\bno\b', prompt_norm))
    learner_has_no = bool(re.search(r'\bno\b', learner_norm))
    return prompt_has_no != learner_has_no

