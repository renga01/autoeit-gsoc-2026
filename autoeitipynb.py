def normalise(text):
    """Lowercase, strip accents for comparison, remove punctuation."""
    if not isinstance(text, str):
        return ""
    # Remove transcription artifacts: false starts in [], dashes, filler markers
    text = re.sub(r'\[.*?\]', ' ', text)   # remove bracketed content
    text = re.sub(r'-\w+', ' ', text)       # remove cut-off words (word-)
    text = re.sub(r'\bXXX+\b', ' ', text, flags=re.IGNORECASE)
    text = re.sub(r'[¿¡?!,.:;"\']', ' ', text)
    text = text.lower().strip()
    # Strip accents (for overlap matching — á→a etc.)
    nfd = unicodedata.normalize('NFD', text)
    return ''.join(c for c in nfd if unicodedata.category(c) != 'Mn')


def tokenise(text):
    """Split into word tokens."""
    return [w for w in re.split(r'\s+', normalise(text)) if w]

