# 5. CORE SCORING FUNCTION
# ─────────────────────────────────────────────

def score_utterance(stimulus, transcription):
    """
    Apply the meaning-based rubric and return a score 0–4.

    Rubric logic:
      0: Silence / unintelligible / only 1 content word
      1: ≤ 40% content word overlap OR very few tokens
      2: 41–65% overlap OR meaning slightly changed
         (Also: negation flip, antonym substitution, major structural loss)
      3: 66–90% overlap AND meaning preserved
         (Minor grammar changes, synonyms, small omissions OK)
      4: > 90% overlap AND no meaning changes AND no structural loss
         (Near-perfect repetition; false starts, filler do not penalise)
    """
    # ── Score 0 ──────────────────────────────
    if is_score_zero(transcription):
        return 0, "Silence/unintelligible/minimal"

    prompt_norm  = normalise(stimulus)
    learner_norm = normalise(transcription)

    prompt_cw  = content_words(stimulus)
    learner_cw = content_words(transcription)

    if not prompt_cw:
        return 4, "Prompt has no content words"

    # Overlap ratio: what fraction of prompt content words appear in learner?
    overlap     = len(prompt_cw & learner_cw)
    total_union = len(prompt_cw | learner_cw)
    overlap_ratio = overlap / len(prompt_cw)  # recall-style: how much of prompt is covered

    # ── Score 0 edge: only 1 content word in learner ──
    if len(learner_cw) <= 1:
        return 0, f"Only {len(learner_cw)} content word(s) in learner response"

    # ── Meaning-change checks ─────────────────
    meaning_flipped  = has_meaning_flip(prompt_norm, learner_norm)
    negation_changed = negation_added_or_removed(prompt_norm, learner_norm)

    # ── Score 4 ──────────────────────────────
    if overlap_ratio >= 0.90 and not meaning_flipped and not negation_changed:
        return 4, f"Near-perfect ({overlap_ratio:.0%} overlap, no meaning changes)"

    # ── Score 3 ──────────────────────────────
    if overlap_ratio >= 0.66 and not meaning_flipped and not negation_changed:
        return 3, f"Meaning preserved ({overlap_ratio:.0%} overlap)"

    # ── Score 2 ──────────────────────────────
    if overlap_ratio >= 0.40:
        if meaning_flipped:
            return 2, f"Meaning changed - antonym substitution ({overlap_ratio:.0%} overlap)"
        if negation_changed:
            return 2, f"Negation added/removed ({overlap_ratio:.0%} overlap)"
        return 2, f"Partial meaning ({overlap_ratio:.0%} overlap)"

    # ── Score 1 ──────────────────────────────
    if overlap_ratio > 0.15:
        return 1, f"Limited content preserved ({overlap_ratio:.0%} overlap)"

    # ── Score 0 ──────────────────────────────
    return 0, f"Insufficient content ({overlap_ratio:.0%} overlap)"

